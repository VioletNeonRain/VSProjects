#include<iostream>
using namespace std;
int main()
{
	int n = 0;
	unsigned char b1[16] = { 0xd1,0x74,0xab,0x98,0xd2,0x77,0xd9,0xf5,0xa5,0x61,0x1c,0x2c,0x9f,0x41,0x9d,0x9f };
	unsigned char b2[16] = { 0x0f,0xdf,0x05,0x8f,0x65,0x1c,0x58,0x27,0x6f,0x26,0x92,0x9d,0x20,0x32,0x21,0xda };
	for (int i = 0; i < 16; i++)
	{
		char a = b1[i]^b2[i];
		int temp;
		for (int j = 0; j < 8; j++)
		{
			temp = (a >> j) & 0x1;
			if (temp == 1)
				n++;
		}
	}
	cout << n << endl;
	return 0;
}